<template>
  <div class="chat">
    <chat-log/>
    <message-input/>
  </div>
</template>

<script>
/* reference: https://www.pubnub.com/blog/vuejs-group-chat-app-tutorial/ */
import ChatLog from '@/components/chat/ChatLog';
import MessageInput from '@/components/chat/MessageInput';
import PubNubVue from 'pubnub-vue';
import {mapGetters} from 'vuex';

export default {
  name: 'chat-container',
  components: {
    ChatLog,
    MessageInput
  },
  mounted() {
    // Subscribe to PubNub
    this.$pnSubscribe({
      channels: ['vueChat'],
    });
    this.$nextTick(fetchHistory(this.$store));
  },
  computed: {
    ...mapGetters({
      uuid: 'getMyUuid',
    }),
  },
};

function fetchHistory(store){
  PubNubVue.getInstance().history(
      {
        channel: 'vueChat',
        count: 6, // how many items to fetch
        stringifiedTimeToken: true, // false is the default
      },
      function(status, response) {
        const msgs = response.messages;
        console.log(msgs);
        // Iterate msgs array and save each element to history
        msgs.forEach(elem => {
          store.commit('addHistory', {history: [elem]});
        })
      }
    )
}
</script>

